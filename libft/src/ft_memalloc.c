#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*s;
	size_t	i;

	s = (void *)malloc(size);
	if (s)
	{
		i = 0;
		while (i < size)
		{
			((char *)s)[i] = '\0';
			i++;
		}
		return (s);
	}
	return (0);
}
