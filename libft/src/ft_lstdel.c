#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*a;
	t_list	*b;

	if (*alst && del)
	{
		b = *alst;
		while (b)
		{
			a = b;
			b = b->next;
			del(a->content, a->content_size);
			free(a);
		}
		*alst = 0;
	}
}
